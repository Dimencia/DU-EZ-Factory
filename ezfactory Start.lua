function tablelength(T)
  local count = 0
  for _ in pairs(T) do count = count + 1 end
  return count
end

IndustryUnits = {}
local industryBySchematic = {}

CategoryView = true

local ids = core.getElementIdList()
for _,id in ipairs(ids) do
    local statusString = core.getElementIndustryStatus(id):gsub("^%s*(.-)%s*$", "%1") -- Trim
    if statusString ~= "{}" then
        if core.getElementTypeById(id) ~= "Transfer Unit" then -- TUs don't have schems or give any info
			local status = json.decode(core.getElementIndustryStatus(id))
			local name = SchematicNames[status.schematicId] -- Only keep ones that have a valid schematic we can read
			if name ~= nil and name ~= "" then
				table.insert(IndustryUnits, {name=core.getElementNameById(id), id=id, schematic=name})
				-- And, as an alternative display, group them by schematic name
				if industryBySchematic[name] == nil then
					industryBySchematic[name] = { {name=core.getElementNameById(id), id=id, schematic=name} }
				else
					table.insert(industryBySchematic[name], {name=core.getElementNameById(id), id=id, schematic=name})
				end
			end
        end
    end
end

-- Now build a global IndustryBySchematic that's actually usable
IndustryBySchematic = {}
for k,v in pairs(industryBySchematic) do
	table.insert(IndustryBySchematic, v)
end


-- And sort them by name too
function industryCompare(a, b)
	return a.schematic < b.schematic
end
function IndustrySchematicCompare(a, b)
	return industryCompare(a[1], b[1]) -- This is fine
end


table.sort(IndustryBySchematic,IndustrySchematicCompare)
table.sort(IndustryUnits,industryCompare)



